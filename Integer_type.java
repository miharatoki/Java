class Integer_type {
  public static void main(String[] args){
    // 整数

    // byte型は-100~100
    // short型は-3万~3万
    // int型は-20億~20億
    // long型は京以上の桁(最後にLが必要)
    byte var01 = 1;  
    short var02 = 12345;
    int var03 = 1234567890;
    long var04 = 123456789000L;

    System.out.println(var01);
    System.out.println(var02);
    System.out.println(var03);
    System.out.println(var04);


  }
}